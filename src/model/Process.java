package model;

public class Process {

    String id;
    int wt;
    int priority;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getWt() {
        return wt;
    }

    public void setWt(int wt) {
        this.wt = wt;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }
}
